def _parse_args(argv: Optional[List[str]] = None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        prog="expansion_agent",
        description="TownEye UMF — Autonomous Expansion Engine (Patch #175)",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=textwrap.dedent("""
            Examples:
              # Interactive — ask for next 10, then prompt to continue
              python core/expansion_agent.py

              # Batch — onboard 50 towns and exit
              python core/expansion_agent.py --batch 50

              # Custom seed, Anthropic backend, dry-run
              python core/expansion_agent.py \\
                  --seed "Cambridge MA,Medford MA,Malden MA" \\
                  --provider anthropic \\
                  --dry-run

              # Gemini backend
              python core/expansion_agent.py \\
                  --provider gemini \\
                  --model gemini-2.5-pro
        """),
    )
    parser.add_argument(
        "--seed",
        type=str,
        default="Arlington MA,Somerville MA,Burlington MA,Lexington MA,Bedford MA",
        help="Comma-separated list of seed towns (default: 5 Arlington-adjacent MA towns)",
    )
    parser.add_argument(
        "--batch",
        type=int,
        default=None,
        metavar="N",
        help="Onboard N towns total and exit (non-interactive)",
    )
    parser.add_argument(
        "--per-round",
        type=int,
        default=10,
        metavar="N",
        help="Towns to request per LLM call (default: 10)",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Print scaffolded configs to stdout; do not write any files",
    )
    parser.add_argument(
        "--provider",
        choices=["gemini", "openai", "anthropic"],
        default=None,
        help=(
            "LLM backend (auto-detected from env vars when omitted). "
            "Priority: gemini > openai > anthropic."
        ),
    )
    parser.add_argument(
        "--model",
        type=str,
        default=None,
        help=(
            "Override LLM model name "
            "(defaults: gemini-2.5-flash | gpt-4o-mini | claude-3-5-haiku-20250219)"
        ),
    )
    parser.add_argument(
        "--show-registry",
        action="store_true",
        help="Print the current expansion registry and exit",
    )
    return parser.parse_args(argv)


def main(argv: Optional[List[str]] = None) -> None:
    logging.basicConfig(
        level=logging.INFO,
        format="%(asctime)s | %(levelname)-8s | %(name)s | %(message)s",
        datefmt="%Y-%m-%dT%H:%M:%S",
        stream=sys.stdout,
    )

    args = _parse_args(argv)

    if args.show_registry:
        registry = _load_registry()
        print(json.dumps(registry, indent=2))
        return

    seed_towns = [t.strip() for t in args.seed.split(",") if t.strip()]

    run_expansion(
        seed_towns=seed_towns,
        batch_total=args.batch,
        per_round=args.per_round,
        dry_run=args.dry_run,
        provider=args.provider,
        model=args.model,
    )


if __name__ == "__main__":
    main()

