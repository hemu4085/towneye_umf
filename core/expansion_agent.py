def _onboarded_slugs(registry: Dict[str, Any]) -> List[str]:
    return [entry["town_slug"] for entry in registry.get("onboarded", [])]


def _onboarded_display_names(registry: Dict[str, Any]) -> List[str]:
    return [
        f"{e.get('town_name', '?')}, {e.get('state', '?')}"
        for e in registry.get("onboarded", [])
    ]


def run_expansion(
    seed_towns: List[str],
    batch_total: Optional[int] = None,
    per_round: int = 10,
    dry_run: bool = False,
    provider: Optional[str] = None,
    model: Optional[str] = None,
) -> None:
    """
    Main loop — keep asking the LLM for more towns until *batch_total* is
    reached (batch mode) or the user declines to continue (interactive mode).

    Parameters
    ----------
    seed_towns : list[str]
        Initial seed list, e.g. ``["Arlington, MA", "Lexington, MA"]``.
    batch_total : int, optional
        If set, run until this many NEW towns have been onboarded.  If None,
        runs one round then prompts to continue interactively.
    per_round : int
        Number of towns to request per LLM call (default 10).
    dry_run : bool
        Print scaffolded configs to stdout; do not write files.
    provider : str, optional
        ``"openai"`` or ``"anthropic"``.  Auto-detected when omitted.
    model : str, optional
        Override LLM model identifier.
    """
    registry = _load_registry()
    total_onboarded_this_session = 0

    # Resolve the effective provider without raising — we only need the key
    # to be present when an actual LLM call is made inside the loop.
    effective_provider = provider
    if effective_provider is None:
        if os.environ.get("GEMINI_API_KEY"):
            effective_provider = "gemini"
        elif os.environ.get("OPENAI_API_KEY"):
            effective_provider = "openai"
        elif os.environ.get("ANTHROPIC_API_KEY"):
            effective_provider = "anthropic"
        else:
            effective_provider = "gemini"   # will raise with a clear message at call-time

    _model_defaults = {
        "gemini":    _GEMINI_DEFAULT_MODEL,
        "openai":    _OPENAI_DEFAULT_MODEL,
        "anthropic": _ANTHROPIC_DEFAULT_MODEL,
    }
    effective_model = model or os.environ.get(
        "TOWNEYE_LLM_MODEL",
        _model_defaults.get(effective_provider, _OPENAI_DEFAULT_MODEL),
    )

    logger.info("expansion_agent | Starting expansion loop")
    logger.info("expansion_agent | Seed towns  : %s", seed_towns)
    logger.info("expansion_agent | Batch target: %s", batch_total or "interactive")
    logger.info("expansion_agent | Per-round   : %d", per_round)
    logger.info("expansion_agent | Dry run     : %s", dry_run)

    while True:
        already_names = _onboarded_display_names(registry)
        # Don't re-suggest towns already in the seed list
        all_known = list(set(seed_towns) | set(already_names))

        remaining = (
            batch_total - total_onboarded_this_session
            if batch_total else per_round
        )
        this_round = min(per_round, remaining) if batch_total else per_round

        if this_round <= 0:
            logger.info("expansion_agent | Batch target reached — exiting.")
            break

        # --- LLM call ---
        try:
            suggested = _ask_llm(
                seed_towns=seed_towns,
                already_onboarded=all_known,
                n=this_round,
                provider=effective_provider,
                model=effective_model,
            )
        except (RuntimeError, ValueError) as exc:
            logger.error("expansion_agent | LLM call failed: %s", exc)
            break

        # --- Scaffold + register ---
        round_results = []
        for town in suggested:
            slug = _make_slug(town["town_name"], town["state"])

            if slug in _onboarded_slugs(registry):
                logger.info("expansion_agent | %s already onboarded — skipping.", slug)
                continue

            config_path = _scaffold_config(
                town=town,
                seed_source=", ".join(seed_towns[:3]) + (" ..." if len(seed_towns) > 3 else ""),
                llm_model=effective_model,
                dry_run=dry_run,
            )

            entry = {
                "town_slug":  slug,
                "town_name":  town["town_name"],
                "state":      town["state"],
                "lat":        town.get("lat"),
                "lon":        town.get("lon"),
                "geo_hash":   _compute_geohash(float(town["lat"]), float(town["lon"])),
                "config_path": str(config_path),
                "rationale":  town.get("rationale", ""),
            }
            if not dry_run:
                registry["onboarded"].append(entry)
            round_results.append(entry)
            total_onboarded_this_session += 1

        # Persist registry
        if not dry_run:
            registry.setdefault("rounds", []).append({
                "timestamp": time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime()),
                "model": effective_model,
                "added": [r["town_slug"] for r in round_results],
            })
            _save_registry(registry)

        # --- Summary ---
        print(f"\n{'═'*62}")
        print(f"  Round complete — {len(round_results)} town(s) {'(DRY-RUN) ' if dry_run else ''}onboarded this round")
        print(f"  Total this session : {total_onboarded_this_session}")
        print(f"  Total in registry  : {len(registry.get('onboarded', []))}")
        print(f"{'═'*62}")
        for r in round_results:
            geo = r.get("geo_hash", "?")
            print(f"  ✓  {r['town_slug']:<30}  geo_hash={geo}  [{r.get('rationale','')[:55]}]")
        print()

        if batch_total and total_onboarded_this_session >= batch_total:
            logger.info("expansion_agent | Batch target of %d reached.", batch_total)
            break

        # Interactive prompt
        if not batch_total:
            try:
                answer = input("Continue with the next round? [Y/n] ").strip().lower()
            except EOFError:
                # Non-interactive environment (e.g. piped input) — stop
                break
            if answer in ("n", "no"):
                break


