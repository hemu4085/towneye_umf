def _scaffold_config(
    town: Dict[str, Any],
    seed_source: str,
    llm_model: str,
    dry_run: bool = False,
) -> pathlib.Path:
    """
    Write ``configs/{town_slug}/config.yaml`` for a new town.

    Parameters
    ----------
    town : dict
        Parsed LLM town object with at minimum ``town_name``, ``state``,
        ``lat``, ``lon``.
    seed_source : str
        Human-readable label for the origin of this suggestion (e.g. the
        original seed list), written into ``expansion_metadata.rationale``.
    llm_model : str
        Model identifier stamped into ``expansion_metadata.llm_model``.
    dry_run : bool
        When True, print the config to stdout without writing any files.

    Returns
    -------
    pathlib.Path
        Path to the config file (written or would-be-written in dry_run mode).
    """
    town_name     = str(town.get("town_name", "Unknown")).strip()
    state         = str(town.get("state", "XX")).strip().upper()
    county        = str(town.get("county", "")).strip()
    fips_code     = str(town.get("fips_code", "")).strip()
    lat           = float(town.get("lat", 0.0))
    lon           = float(town.get("lon", 0.0))
    pop_tier      = str(town.get("population_tier", "unknown")).strip()
    rationale     = str(town.get("rationale", "")).replace('"', "'").strip()
    town_slug     = _make_slug(town_name, state)
    geo_hash      = _compute_geohash(lat, lon)

    # Derive a simple domain placeholder from slug (e.g. "waltham-ma.gov")
    town_slug_domain = f"{town_slug}.gov"

    generated_at = time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime())

    rendered = _CONFIG_TEMPLATE.format(
        town_slug=town_slug,
        town_name=town_name,
        state=state,
        state_lower=state.lower(),
        county=county,
        fips_code=fips_code,
        lat=lat,
        lon=lon,
        geo_hash=geo_hash,
        pop_tier=pop_tier,
        population_tier=pop_tier,
        rationale=rationale,
        llm_model=llm_model,
        seed_source=seed_source,
        generated_at=generated_at,
        town_slug_domain=town_slug_domain,
    )

    config_path = _CONFIGS_DIR / town_slug / "config.yaml"

    if dry_run:
        print(f"\n{'─'*60}")
        print(f"DRY-RUN | Would write: {config_path}")
        print(f"{'─'*60}")
        print(rendered[:800], "...(truncated)")
        return config_path

    config_path.parent.mkdir(parents=True, exist_ok=True)
    with open(config_path, "w") as f:
        f.write(rendered)

    logger.info("expansion_agent | Scaffolded → %s", config_path)
    return config_path


