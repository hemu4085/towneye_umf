def _select_llm_client() -> str:
    """
    Return the best available provider string based on set env vars.

    Priority: gemini > openai > anthropic.
    Raises ``RuntimeError`` if no key is found.
    """
    from core.llm_client import select_provider  # type: ignore[import]
    return select_provider()


def _ask_llm(
    seed_towns: List[str],
    already_onboarded: List[str],
    n: int = 10,
    provider: Optional[str] = None,
    model: Optional[str] = None,
) -> List[Dict[str, Any]]:
    """
    Ask the LLM for the next *n* towns to onboard.

    Returns a list of parsed town dicts.  Raises ``ValueError`` if the LLM
    response cannot be parsed as a JSON array of the expected shape.
    """
    from core.llm_client import call_llm  # type: ignore[import]

    if provider is None:
        provider = _select_llm_client()

    if model is None:
        model = os.environ.get("TOWNEYE_LLM_MODEL") or None  # let call_llm pick the default

    system_prompt = _SYSTEM_PROMPT.replace("{n}", str(n))
    user_prompt = _USER_PROMPT_TEMPLATE.format(
        seed_list="\n".join(f"  - {t}" for t in seed_towns),
        already_onboarded=(
            "\n".join(f"  - {t}" for t in already_onboarded)
            if already_onboarded else "  (none yet)"
        ),
        n=n,
    )

    logger.info("expansion_agent | Querying %s for next %d towns ...", provider, n)
    t0 = time.perf_counter()

    raw = call_llm(
        system=system_prompt,
        user=user_prompt,
        provider=provider,
        model=model,
        n_tokens=_LLM_MAX_TOKENS,
    )

    elapsed = time.perf_counter() - t0
    logger.info("expansion_agent | LLM responded in %.2fs", elapsed)

    # Strip optional markdown fences the model sometimes adds despite instructions
    raw = re.sub(r"^```(?:json)?\s*", "", raw, flags=re.IGNORECASE)
    raw = re.sub(r"\s*```$", "", raw)

    try:
        towns = json.loads(raw)
    except json.JSONDecodeError as exc:
        raise ValueError(
            f"expansion_agent | LLM returned non-JSON response: {exc}\n"
            f"Raw output (first 500 chars): {raw[:500]}"
        ) from exc

    if not isinstance(towns, list):
        raise ValueError(
            f"expansion_agent | Expected JSON array, got {type(towns).__name__}."
        )

    required_fields = {"town_name", "state", "lat", "lon"}
    for i, town in enumerate(towns):
        missing = required_fields - set(town.keys())
        if missing:
            raise ValueError(
                f"expansion_agent | Town #{i} missing required fields: {missing}. "
                f"Got: {list(town.keys())}"
            )

    return towns


