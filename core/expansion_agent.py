# Slug + geohash helpers
# ---------------------------------------------------------------------------

def _make_slug(town_name: str, state: str) -> str:
    """
    Derive a kebab-case ``town_slug`` from a town name and state abbreviation.

    Examples
    --------
    ``"Waltham", "MA"``  →  ``"waltham-ma"``
    ``"New Bedford", "MA"``  →  ``"new-bedford-ma"``
    """
    raw = f"{town_name} {state}".lower()
    slug = re.sub(r"[^a-z0-9]+", "-", raw).strip("-")
    return slug


def _compute_geohash(lat: float, lon: float, precision: int = 6) -> str:
    """
    Encode a WGS-84 lat/lon pair to a geohash string at *precision* characters.

    Falls back to a deterministic placeholder when ``geohash2`` is not installed
    so the engine stays runnable in environments that haven't yet pip-installed
    the library.
    """
    try:
        import geohash2  # type: ignore[import]
        return geohash2.encode(lat, lon, precision)
    except ImportError:
        logger.warning(
            "expansion_agent | 'geohash2' not installed — using placeholder hash. "
            "Run: pip install geohash2>=1.1"
        )
        # Deterministic fallback: encode lat/lon as base-36 digits
        raw = abs(int(lat * 1000)) + abs(int(lon * 1000))
        chars = "0123456789bcdefghjkmnpqrstuvwxyz"
        result = []
        for _ in range(precision):
            result.append(chars[raw % 32])
            raw //= 32
        return "".join(reversed(result))


