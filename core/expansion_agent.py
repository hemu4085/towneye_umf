"""
TownEye UMF — Autonomous Expansion Engine
==========================================
Self-propagating onboarding system that scales the UMF to 500 towns by
combining an LLM (Gemini, OpenAI, or Anthropic) with config scaffolding.

Architecture
------------
Each iteration of the main loop:

  1. Reads the **expansion registry** (``configs/expansion_registry.json``) to
     know which towns are already onboarded.
  2. Sends the current seed list + already-onboarded list to the LLM and asks
     for the **next 10 adjacent / demographically similar municipalities** to
     onboard.  The LLM returns a structured JSON array — name, state, lat, lon,
     FIPS, population tier, and a one-line rationale.
  3. For each suggested town the engine:
        a. Derives a ``town_slug`` (``waltham-ma`` from ``"Waltham, MA"``).
        b. Calculates a precision-6 **geohash** from the LLM-supplied lat/lon.
        c. Scaffolds ``configs/{town_slug}/config.yaml`` from the master
           template, replacing all town-specific tokens.
        d. Appends the new slug to the registry.
  4. Prints a summary and offers to loop again (interactive mode) or exits
     (``--batch N`` mode).

Zero-Hardcoding contract
------------------------
* ``core/`` logic is entirely generic.  The template that gets stamped into
  each new town config lives in ``_CONFIG_TEMPLATE`` below — it is the only
  place where the config schema is defined.  To add a new config field to
  every onboarded town, add it once to the template.
* The engine never hard-codes any town's values.  All town-specific strings
  are injected from the LLM response JSON.

LLM providers (priority order when ``--provider`` is not specified)
-------------------------------------------------------------------
* **Gemini** — requires ``GEMINI_API_KEY`` env var.
  Uses ``gemini-2.5-flash`` by default.
* **OpenAI** — requires ``OPENAI_API_KEY`` env var.
  Uses ``gpt-4o-mini`` by default.
* **Anthropic** — requires ``ANTHROPIC_API_KEY`` env var.
  Uses ``claude-3-5-haiku-20250219`` by default.

Override with ``--provider gemini|openai|anthropic`` or set
``TOWNEYE_LLM_MODEL`` to override the model name.

Usage
-----
Interactive (ask for the next 10, then prompt to continue)::

    python core/expansion_agent.py

Batch — onboard the next N towns and exit::

    python core/expansion_agent.py --batch 50

Custom seed list with Gemini backend::

    python core/expansion_agent.py \\
        --seed "Cambridge MA,Medford MA,Malden MA" \\
        --provider gemini

Dry-run — print LLM suggestions without writing any files::

    python core/expansion_agent.py --dry-run

Full run to 500 towns::

    python core/expansion_agent.py --batch 500
"""

